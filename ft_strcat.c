char	*ft_strcat(char *s1, const char *s2)
{
	int		id;
	int		is;

	id = 0;
	is = 0;
	while (s1[id] != '\0')
		id++;
	while (s2[is] != '\0')
	{
		s1[id] = s2[is];
		id++;
		is++;
	}
	s1[id] = '\0';
	return (s1);
}
