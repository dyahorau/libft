#include "libft.h"

static long		negate(int n)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (-10000000000000000);
	}
	else
	{
		write(1, "-", 1);
		n = -1 * n;
		return (n);
	}
}

void			ft_putnbr(int n)
{
	int			x;
	char		c;

	if (n < 0 && n >= -2147483648)
		n = negate(n);
	if (n <= 9 && n >= 0)
	{
		c = n + '0';
		write(1, &c, 1);
	}
	else if (n >= 10 && n <= 2147483647)
	{
		x = 10;
		while (n / x >= 10)
			x = x * 10;
		while (x >= 1 && n / x >= 0)
		{
			c = n / x + '0';
			write(1, &c, 1);
			n = n - (n / x) * x;
			x = x / 10;
		}
	}
}
